/**
* Cambio de pesetas a euros
*
*@author Raul Martin
*/

public class Ejercicio05 {
  public static void main(String[] args) {
    
    double e = 0.60;
    double p = 100;

    System.out.println(p + " pesetas son " + e + " euros");
    System.out.println((p * 500) + " pesetas son " + ((p * 500) * e) + " euros");
  }
}
